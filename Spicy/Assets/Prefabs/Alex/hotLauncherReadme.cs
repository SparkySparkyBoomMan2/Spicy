/*


README for hotLauncher prefab


     Prefab for a hotsauce grenade launcher asset! --- originally made for Super Spicy Gun Game
        * Weapon is controlled by the script attatched
            - script has multiple public objects and variables 
                * Weapon renderer - holds the sprite renderer of the weapon to handle aim manipulation
                * Animator - Animator for the weapon to handle idle and firing animations
                * Projectile - the bullet which is shot out from the launcher (in this case a hot-sauce grenade)
                * FireRateDelay - public variable which allows us to change the rate of fire of the launcher

        * The exact details of the inner workings of the launcher are within its own script
            - "gun_HotSauce_launcher"

*/